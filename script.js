function resizeCanvas(){canvas.width=innerWidth*devicePixelRatio;canvas.height=innerHeight*devicePixelRatio;ctx.setTransform(devicePixelRatio,0,0,devicePixelRatio,0,0)}
function createPetals(count=16){for(let i=0;i<count;i++)petals.push({x:Math.random()*innerWidth,y:-30-Math.random()*innerHeight*.6,s:5+Math.random()*9,v:1+Math.random()*2.5,w:Math.random()*6,t:Math.random()*6.28,o:.45+Math.random()*.5})}
function drawPetals(){ctx.clearRect(0,0,innerWidth,innerHeight);petals.forEach(p=>{p.y+=p.v;p.x+=Math.sin(p.t+=.025)*.7+p.w*.02;ctx.save();ctx.translate(p.x,p.y);ctx.rotate(p.t);ctx.globalAlpha=p.o;ctx.fillStyle=p.s>9?"#d81730":"#f34b5e";ctx.beginPath();ctx.ellipse(0,0,p.s*.55,p.s,0,0,Math.PI*2);ctx.fill();ctx.restore()});petals=petals.filter(p=>p.y<innerHeight+30);requestAnimationFrame(drawPetals)}
