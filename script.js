const finalWords = ["Tú", "eres", "mi", "lugar", "favorito", "para", "siempre."];
const openedBlooms = new Set();
document.querySelectorAll(".bloom").forEach((bloom, index) => bloom.addEventListener("click", () => {
  if (openedBlooms.has(index)) return;
  openedBlooms.add(index);
  bloom.classList.add("opened");
  bloom.setAttribute("aria-pressed", "true");
  const word = document.createElement("span");
  word.textContent = finalWords[index];
  word.style.order = index;
  document.querySelector("#loveWords").appendChild(word);
  document.querySelector("#bloomCount").textContent = openedBlooms.size;
  window.gardenSoundscape?.chime();
  createPetals(8);
  if (openedBlooms.size === 7) unlockFinale();
}));

function unlockFinale() {
  const garden = document.querySelector("#finalGarden");
  garden.classList.add("complete");
  createPetals(55);
  setTimeout(() => {
    const reveal = document.querySelector("#finalReveal");
    reveal.setAttribute("aria-hidden", "false");
    document.querySelector(".cantarina-stage").scrollIntoView({ behavior:"smooth" });
  }, 2300);
}

