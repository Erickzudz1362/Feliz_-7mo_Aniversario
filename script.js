const scenes = document.querySelectorAll(".scene");
const navDots = document.querySelectorAll(".nav-dots a");
const sceneObserver = new IntersectionObserver(entries => entries.forEach(entry => {
  if (!entry.isIntersecting) return;
  entry.target.classList.add("in-view");
  const number = Number(entry.target.dataset.scene);
  window.gardenSoundscape?.setLevel(number === 9 ? .025 : .24);
  if (number >= 1 && number <= 7) {
    document.querySelector("#currentChapter").textContent = `0${number}`;
    document.querySelector("#navProgress").style.height = `${number / 7 * 100}%`;
    navDots.forEach((dot, index) => dot.classList.toggle("active", index === number - 1));
  }
}), { threshold:.55 });
scenes.forEach(scene => sceneObserver.observe(scene));

let ticking = false;
window.addEventListener("scroll", () => {
  if (ticking || matchMedia("(prefers-reduced-motion: reduce)").matches) return;
  ticking = true;
  requestAnimationFrame(() => {
    document.querySelectorAll("[data-parallax]").forEach(layer => {
      const rect = layer.parentElement.getBoundingClientRect();
      if (rect.bottom > 0 && rect.top < innerHeight) layer.style.translate = `0 ${rect.top * -.035}px`;
    });
    ticking = false;
  });
}, { passive:true });

